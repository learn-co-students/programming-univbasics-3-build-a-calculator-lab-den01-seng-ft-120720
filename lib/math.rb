def addition(num1, num2)
  add = num1 + num2 
  return add 
end

def subtraction(num1, num2)
  subtract = num1 - num2
  return subtract
end

def division(num1, num2)
  divise = num1 / num2
  return divise
end

def multiplication(num1, num2)
  multiply = num1 * num2
  return multiply
end

def modulo(num1, num2)
  mod = num1 % num2
  return mod
end

def square_root(num)
  square = Math.sqrt(num)
  return square
end

